//To check That something is equal to something
const num1 = 5;
const num2 = "5";
// "=="this just checks the value of both the variables that are compared
console.log(num1==num2); //true

//"===" this checks value as well as well type
console.log(num1===num2); //false




//NOT EQUAL TO
// this checks just values
console.log(num1!=num2);  //false
//this checks type as well
console.log(num1!==num2); //true