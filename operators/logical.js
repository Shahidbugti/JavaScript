// LOGICAL OPERATORS ===> Logical operators are used to check whether one or more expressions result in either true or false.

//  Can only return booleon values

// There are three logical operators in js

// 1-) Logical AND (&&)
// It returns true if both oprerands are true otherwise false
console.log(7 > 2 && 2 > 1);//True


let a = 5;
let b;
if(a && b){
  console.log("Yes")
}
else{
  console.log("false")
} //FALSE

//Logical OR (||)
//It returns if atleast one of Operands is true otherwise false
let c = 5;
let d;
if(c && d){
  console.log("Yes")
}
else{
  console.log("false")
}
//true
